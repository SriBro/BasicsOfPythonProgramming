string1 = "this is me Sri"
#slicing
#All mean the same
print(string1[0:2])
print(string1[:2])
print(string1[:-12])

#both mean the same
print(string1[2:14])
print(string1[2:])

#Making the first letter of the first word as capital
print(string1.capitalize())
print(string1.find("this"))
print(string1.replace("this","that"))
